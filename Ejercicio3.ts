//TypeScript sirve para agregar tipado y otras características orientadas a la programación orientada a objetos

// Declaración de una variable con tipo
let numero: number = 42;

// Declaración de una función con tipos
function saludar(nombre: string): string {
  return `Hola, ${nombre}!`;
}

// Uso de la variable y la función
console.log(numero);
console.log(saludar("Juan"));
